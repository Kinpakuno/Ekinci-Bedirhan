CREATE TABLE categorie (
    id INT,
    libelle VARCHAR(20),
    PRIMARY KEY (id)
);


CREATE TABLE produit (
    ref INT,
    nom VARCHAR(30),
    prix Float,
    idCategorie INT,
    PRIMARY KEY (ref),
    FOREIGN KEY (idCategorie) REFERENCES categorie(id)
);

CREATE TABLE paiement (
    id INT,
    libelle VARCHAR(20) NOT NULL,
    PRIMARY KEY(id)
);

CREATE TABLE client (
    id INT,
    nom VARCHAR(30) NOT NULL,
    prenom VARCHAR(30) NOT NULL,
    dateNaissance DATE,
    sexe CHAR(1), -- h pour homme et f pour femmec postgres
    parrain INT,
    PRIMARY KEY(id),
    FOREIGN KEY(parrain) REFERENCES client(id)
);

CREATE TABLE adresse (
    id INT,
    idClient INT,
    numero INT,
    voie VARCHAR(10) NOT NULL,
    libelleVoie VARCHAR(30) NOT NULL,
    codePostal VARCHAR(5) NOT NULL,
    ville VARCHAR(30) NOT NULL,

    PRIMARY KEY(id, idClient),
    FOREIGN KEY(idClient) REFERENCES client(id)
);

CREATE TABLE commande (
    id INT,
    idClient INT,
    laDate DATE NOT NULL,
    idAdrLivraison INT,
    idPaiement INT,

    PRIMARY KEY(id),
    FOREIGN KEY(idClient) REFERENCES client(id),
    FOREIGN KEY(idAdrLivraison, idClient) REFERENCES adresse(id, idClient),
    FOREIGN KEY(idPaiement) REFERENCES paiement(id)
);

CREATE TABLE porter(
    idCommande INT,
    refProduit INT,
    quantite INT,

    PRIMARY KEY(idCommande, refProduit),
    FOREIGN KEY(idCommande) REFERENCES commande(id),
    FOREIGN KEY(refProduit) REFERENCES produit(ref)
);

CREATE TABLE passage_caisse (
    id_passage Serial,
    prix float,
    date_passage VARCHAR(10),
    heure VARCHAR(5),
    
    PRIMARY KEY (id_passage)
);

CREATE TABLE jointure_produit_passage (
    id_jointure int,
    id_passage_caisse int,
    id_produit int,
    
    PRIMARY KEY (id_jointure),
    FOREIGN KEY (id_passage_caisse) REFERENCES passage_caisse(id_passage)
);

